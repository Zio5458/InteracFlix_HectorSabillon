    private void bot_regMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bot_regMouseClicked
        Usuario usuario = new Usuario();
        usuario.setNombre(text_u.getText());
        usuario.setContra(text_contra.getText());
        usuario.setCorreo(text_correo.getText());
        usuario.setEdad((int) reg_edad.getValue());
        usuario.setGenero(((String) reg_gen.getSelectedItem()).charAt(0));
        if (usuario.getNombre() != null
                && usuario.getContra() != null
                && usuario.getCorreo() != null
                && usuario.getEdad() != -1) {
            usuarios.add(usuario);
            JOptionPane.showMessageDialog(this, "Se ha registrado exitosamente");
            try {
                actualizarData();
            } catch (IOException ex) {
                Logger.getLogger(Loading.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_bot_regMouseClicked

