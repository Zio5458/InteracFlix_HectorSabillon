    public void actualizarData() throws IOException {
        File file = null;
        FileWriter writer = null;
        BufferedWriter buffer = null;
        try {
            file = new File("./datausuarios.txt");
            writer = new FileWriter(file, false);
            buffer = new BufferedWriter(writer);
            buffer.write(AL_to_String());
            buffer.newLine();
            buffer.flush();
        } catch (Exception e) {
            Logger.getLogger(Loading.class.getName()).log(Level.SEVERE, null, e);
        }
        buffer.close();
        writer.close();
    }

    public String AL_to_String() {
        String temp = "";
        for (int i = 0; i < usuarios.size(); i++) {
            temp += usuarios.get(i).getNombre() + ",";
            temp += usuarios.get(i).getContra() + "\n";
        }
        return temp;
    }

